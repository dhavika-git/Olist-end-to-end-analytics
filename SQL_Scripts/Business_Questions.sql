-- Create a trigger that stores old order values before updates.

CREATE TABLE order_items_audit (
    audit_id INT AUTO_INCREMENT PRIMARY KEY,
    order_id VARCHAR(50),
    seller_id VARCHAR(50),
    product_id VARCHAR(50),
    price DECIMAL(10,2),
    freight_value DECIMAL(10,2),
    updated_at DATETIME );

DELIMITER //
create trigger stores_old_order_values
BEFORE UPDATE on order_items_dataset
FOR EACH ROW
BEGIN
	INSERT INTO order_items_audit (order_id,seller_id,product_id,price,freight_value,updated_at)
    VALUES(OLD.order_id,OLD.seller_id,OLD.product_id,OLD.price,OLD.freight_value,NOW());
END //
DELIMITER ;

