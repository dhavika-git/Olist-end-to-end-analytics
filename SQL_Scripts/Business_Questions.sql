-- Q12. What percentage growth did revenue experience year-over-year?
SELECT YEAR(order_purchase_timestamp) AS year, SUM(payment_value) AS revenue,
((SUM(payment_value) - LAG(SUM(payment_value)) OVER (ORDER BY YEAR(order_purchase_timestamp))) /
LAG(SUM(payment_value)) OVER (ORDER BY YEAR(order_purchase_timestamp))) * 100 AS yoy_growth_pct
FROM orders_dataset
JOIN order_payments USING (order_id)
GROUP BY year
ORDER BY year; 

