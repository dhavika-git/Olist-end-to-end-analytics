-- Create a trigger that ensures review scores stay between 1 and 5.
DELIMITER //
create trigger Score_review  
BEFORE INSERT ON order_reviews
FOR EACH ROW
BEGIN
	IF NEW.review_score < 1 OR NEW.review_score > 5 THEN
	SIGNAL SQLSTATE '11111'
    SET Message_Text = 'Please enter the value between 1 to 5.';
    END IF;
END //
DELIMITER ;

